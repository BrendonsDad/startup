// JavaScript supports many common programming language conditional constructs. This includes if
// else, and if else. example:

if (a === 1) {
    //...
} else if ( b === 2) {
    //...
} else {
    //...
}

// You can also use the ternary operator. This provides a compact if else representation.

a === 1 ? console.log(1) : console.log('not 1');

